    private void btn_listoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_listoActionPerformed
        int sRow = table_miembros.getSelectedRow();
        if(sRow<0)
            JOptionPane.showMessageDialog(null, "ERROR: selecciona un miembro de la tabla", "Codex", 2);
        else{
            if(vrPrestamo!=null){
                vrPrestamo.sMiembro = (String) table_miembros.getValueAt(sRow, 0);
                String nMiembro = (String) table_miembros.getValueAt(sRow, 2);
                vrPrestamo.establecerMiembro(nMiembro);
            } else if(vmPrestamo!=null){
                vmPrestamo.sMiembro = (String) table_miembros.getValueAt(sRow, 0);
                String nMiembro = (String) table_miembros.getValueAt(sRow, 2);
                vmPrestamo.establecerMiembro(nMiembro);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btn_listoActionPerformed
                                                                                                
