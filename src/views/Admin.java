    private void btn_menuPrincipalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_menuPrincipalMouseClicked
        clickDash = 0;
        returnPreButtonStyle(btn_usuarios, "../icons/icons8_users_30px.png","Segoe UI Semilight", 0, 11);
        btn_usuarios.setBackground(new Color(54,71,89));
        returnPreButtonStyle(btn_prestamos, "../icons/icons8_borrow_book_30px_1.png","Segoe UI Semilight", 0, 11);
        btn_prestamos.setBackground(new Color(54,71,89));
        returnPreButtonStyle(btn_devol, "../icons/icons8_return_30px.png","Segoe UI Semilight", 0, 11);
        btn_devol.setBackground(new Color(54,71,89));
        returnPreButtonStyle(btn_libros, "../icons/icons8_books_35px_1.png","Segoe UI Semilight", 0, 11);
        btn_libros.setBackground(new Color(54,71,89));
        if(pMiembros!=null)
            pMiembros.setVisible(false);
        if(pLibros!=null)
            pLibros.setVisible(false);
        if(pPrestamos!=null)
            pPrestamos.setVisible(false);
        if(pDevoluciones!=null)
            pDevoluciones.setVisible(false);
        btn_menuPrincipal.setVisible(false);
        main_menu.setVisible(true);
    }//GEN-LAST:event_btn_menuPrincipalMouseClicked

